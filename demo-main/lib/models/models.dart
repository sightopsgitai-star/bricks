/// Model representing a company in the industrial monitoring system.
/// Each company has multiple machines and production facilities.
class Company {
  final String id;
  final String name;
  final String industry;
  final String location;
  final int totalMachines;

  /// True if the server received machine telemetry within the last 15 minutes.
  final bool isOnline;

  /// Date of the most recent production_history record for this client.
  final DateTime? lastSeenDate;

  final String? latestDowntimeReason;
  final String? latestDowntimeDescription;

  const Company({
    required this.id,
    required this.name,
    required this.industry,
    required this.location,
    required this.totalMachines,
    this.isOnline = false,
    this.lastSeenDate,
    this.latestDowntimeReason,
    this.latestDowntimeDescription,
  });

  @override
  String toString() => name;
}

/// Represents the operational status of a machine.
enum MachineStatus { running, stopped, maintenance }

/// Model representing an industrial machine.
/// Contains operational data like cycle counts, downtime, and status.
class Machine {
  final String id;
  final String name;
  final String type;
  final String companyId;
  final MachineStatus status;
  final int cycleCount;
  final double cureCycleTime; // in minutes
  final int downtimeMinutes;
  final int startCount;
  final int stopCount;
  final DateTime lastMaintenance;
  final double temperature;
  final double efficiency; // percentage

  /// Live motor current in Amperes (null if no sensor).
  final double? motorCurrent;

  /// Live motor speed in RPM (null if no sensor).
  final double? motorSpeedRpm;

  /// True when admin has disabled this machine for this client.
  /// Client UI must not allow enabling it.
  final bool adminDisabled;

  /// True when VFD fault is active on this machine.
  final bool vfdFault;

  /// Live running bit from PLC (null if not available).
  final bool? runBit;

  const Machine({
    required this.id,
    required this.name,
    required this.type,
    required this.companyId,
    required this.status,
    required this.cycleCount,
    required this.cureCycleTime,
    required this.downtimeMinutes,
    required this.startCount,
    required this.stopCount,
    required this.lastMaintenance,
    required this.temperature,
    required this.efficiency,
    this.motorCurrent,
    this.motorSpeedRpm,
    this.adminDisabled = false,
    this.vfdFault = false,
    this.runBit,
  });

  /// Combined start+stop count for backward compat
  int get startStopCount => startCount + stopCount;

  /// Check if machine is currently running
  bool get isRunning => status == MachineStatus.running;

  /// Check if downtime is critically high (over 60 minutes)
  bool get hasHighDowntime => downtimeMinutes > 60;

  /// Get status color indicator
  String get statusLabel {
    switch (status) {
      case MachineStatus.running:
        return 'Running';
      case MachineStatus.stopped:
        return 'Stopped';
      case MachineStatus.maintenance:
        return 'Maintenance';
    }
  }
}

/// Severity levels for alerts in the monitoring system.
enum AlertSeverity { low, medium, high, critical }

/// Types of alerts that can be generated by the system.
enum AlertType {
  machineStopped,
  highDowntime,
  temperatureWarning,
  maintenanceDue,
  productionTargetMissed,
  abnormalCondition,
  lowEfficiency,
  emergencyTriggered,
  vfdFault,
  hydraulicPressureLow,
  controlOff,
  machineStop,
  materialLow,
  hydraulicOff,
  programStop,
}

/// Model representing an alert/notification in the system.
/// Alerts are generated when abnormal conditions are detected.
class Alert {
  final String id;
  final String companyId;
  final String machineId;
  final String machineName;
  final AlertType type;
  final AlertSeverity severity;
  final String message;
  final DateTime timestamp;
  final bool isRead;
  final bool isResolved;

  const Alert({
    required this.id,
    required this.companyId,
    required this.machineId,
    required this.machineName,
    required this.type,
    required this.severity,
    required this.message,
    required this.timestamp,
    this.isRead = false,
    this.isResolved = false,
  });

  /// Get human-readable alert type name
  String get typeName {
    switch (type) {
      case AlertType.machineStopped:
        return 'Machine Stopped';
      case AlertType.highDowntime:
        return 'High Downtime';
      case AlertType.temperatureWarning:
        return 'Temperature Warning';
      case AlertType.maintenanceDue:
        return 'Maintenance Due';
      case AlertType.productionTargetMissed:
        return 'Production Target Missed';
      case AlertType.abnormalCondition:
        return 'Abnormal Condition';
      case AlertType.lowEfficiency:
        return 'Low Efficiency';
      case AlertType.emergencyTriggered:
        return 'Emergency Triggered';
      case AlertType.vfdFault:
        return 'VFD Fault';
      case AlertType.hydraulicPressureLow:
        return 'Hydraulic Pressure Low';
      case AlertType.controlOff:
        return 'Control Off';
      case AlertType.machineStop:
        return 'Machine Stop';
      case AlertType.materialLow:
        return 'Material Low';
      case AlertType.hydraulicOff:
        return 'Hydraulic Off';
      case AlertType.programStop:
        return 'Program Stop';
    }
  }

  /// Get severity label
  String get severityLabel {
    switch (severity) {
      case AlertSeverity.low:
        return 'Low';
      case AlertSeverity.medium:
        return 'Medium';
      case AlertSeverity.high:
        return 'High';
      case AlertSeverity.critical:
        return 'Critical';
    }
  }

  /// Create a copy with updated properties
  Alert copyWith({bool? isRead, bool? isResolved}) {
    return Alert(
      id: id,
      companyId: companyId,
      machineId: machineId,
      machineName: machineName,
      type: type,
      severity: severity,
      message: message,
      timestamp: timestamp,
      isRead: isRead ?? this.isRead,
      isResolved: isResolved ?? this.isResolved,
    );
  }
}

