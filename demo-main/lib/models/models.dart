/// Enum for support ticket status.
enum TicketStatus { open, acknowledged, resolved }

/// Model for a support ticket.
class SupportTicket {
  final int id;
  final String clientId;
  final String clientName;
  final String title;
  final String description;
  final TicketStatus status;
  final DateTime createdAt;
  final DateTime? acknowledgedAt;
  final DateTime? resolvedAt;

  const SupportTicket({
    required this.id,
    required this.clientId,
    required this.clientName,
    required this.title,
    required this.description,
    required this.status,
    required this.createdAt,
    this.acknowledgedAt,
    this.resolvedAt,
  });

  factory SupportTicket.fromJson(Map<String, dynamic> json) {
    return SupportTicket(
      id: json['id'],
      clientId: json['client_id'] ?? '',
      clientName: json['client_name'] ?? 'Unknown Client',
      title: json['title'] ?? '',
      description: json['description'] ?? '',
      status: _parseStatus(json['status']),
      createdAt: DateTime.parse(json['created_at']),
      acknowledgedAt: json['acknowledged_at'] != null ? DateTime.parse(json['acknowledged_at']) : null,
      resolvedAt: json['resolved_at'] != null ? DateTime.parse(json['resolved_at']) : null,
    );
  }

  static TicketStatus _parseStatus(String? s) {
    if (s == 'acknowledged') return TicketStatus.acknowledged;
    if (s == 'resolved') return TicketStatus.resolved;
    return TicketStatus.open;
  }

  String get statusLabel {
    switch (status) {
      case TicketStatus.open: return 'Open';
      case TicketStatus.acknowledged: return 'In Progress';
      case TicketStatus.resolved: return 'Resolved';
    }
  }

  bool get isOpen => status == TicketStatus.open;
  bool get isAcknowledged => status == TicketStatus.acknowledged;
  bool get isResolved => status == TicketStatus.resolved;

  Duration? get resolutionTime {
    if (acknowledgedAt != null && resolvedAt != null) {
      return resolvedAt!.difference(acknowledgedAt!);
    }
    return null;
  }

  Duration get age {
    return DateTime.now().difference(createdAt);
  }

  Duration? get workDuration {
    if (acknowledgedAt == null) return null;
    final end = resolvedAt ?? DateTime.now();
    return end.difference(acknowledgedAt!);
  }
}
