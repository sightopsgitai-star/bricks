/// Represents a single historical hourly energy point.
class EnergyHistoryPoint {
  final double hour;
  final double kwh;
  final double amps;
  final double pf;

  const EnergyHistoryPoint({
    required this.hour,
    required this.kwh,
    required this.amps,
    required this.pf,
  });

  factory EnergyHistoryPoint.fromJson(Map<String, dynamic> j) {
    return EnergyHistoryPoint(
      hour: (j['hour'] as num? ?? 0.0).toDouble(),
      kwh: (j['energy_kwh'] as num? ?? 0.0).toDouble(),
      amps: (j['overall_amps'] as num? ?? 0.0).toDouble(),
      pf: (j['power_factor'] as num? ?? 0.0).toDouble(),
    );
  }
}

