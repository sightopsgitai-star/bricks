/// Model for holding production statistics over different time periods.
class ProductionStats {
  final String companyId;
  final double dailyProduction;
  final double weeklyProduction;
  final double monthlyProduction;
  final double yearlyProduction;
  final int totalCycles;
  final int todayCycles;
  final double averageCureCycleTime;
  final int currentCycleTimeMs;
  final int lastCycleTimeMs;
  final String activeRecipeName;
  final String blockName;
  final int actualCount;
  final int targetCount;
  final Map<String, dynamic> hourlyBreakdown;
  final int concurrentCalls;
  final int totalDowntimeMinutes;
  final int machinesRunning;
  final int machinesStopped;
  final int machinesInMaintenance;
  final double overallEfficiency;
  final DateTime lastUpdated;
  final List<RawTag> rawTags;

  // ── Live energy data ──────────────────────────────────────────────────
  final EnergyData energy;

  // ── Historical energy data ────────────────────────────────────────────
  final List<EnergyHistoryPoint> todayEnergyHistory;
  final List<EnergyHistoryPoint> yesterdayEnergyHistory;

  const ProductionStats({
    required this.companyId,
    required this.dailyProduction,
    required this.weeklyProduction,
    required this.monthlyProduction,
    required this.yearlyProduction,
    required this.totalCycles,
    required this.todayCycles,
    required this.averageCureCycleTime,
    required this.currentCycleTimeMs,
    required this.lastCycleTimeMs,
    required this.activeRecipeName,
    required this.blockName,
    required this.actualCount,
    required this.targetCount,
    required this.hourlyBreakdown,
    required this.concurrentCalls,
    required this.totalDowntimeMinutes,
    required this.machinesRunning,
    required this.machinesStopped,
    required this.machinesInMaintenance,
    required this.overallEfficiency,
    required this.lastUpdated,
    required this.rawTags,
    this.energy = const EnergyData(),
    this.todayEnergyHistory = const [],
    this.yesterdayEnergyHistory = const [],
  });

  /// Total number of machines
  int get totalMachines =>
      machinesRunning + machinesStopped + machinesInMaintenance;

  /// Total start+stop combined (deprecated)
  int get totalStartStops => actualCount;

  /// Format downtime as hours and minutes
  String get formattedDowntime {
    final hours = totalDowntimeMinutes ~/ 60;
    final minutes = totalDowntimeMinutes % 60;
    if (hours > 0) {
      return '${hours}h ${minutes}m';
    }
    return '${minutes}m';
  }
}

